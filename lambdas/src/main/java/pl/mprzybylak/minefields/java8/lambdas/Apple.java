package pl.mprzybylak.minefields.java8.lambdas;

public class Apple {
	
	private int weight;
	private int size;

	public Apple() {
	}
	
	public Apple(int weight, int size) {
		this.weight = weight;
		this.size = size;
	}

	public int getWeight() {
		return weight;
	}
	
	public int getSize() {
		return size;
	}
	
}
